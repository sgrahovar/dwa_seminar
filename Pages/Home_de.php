<h2>Info zum Restaurant</h2>

<p>
Restaurant Dvori kralja Tomislava immer wieder überraschen Sie mit ihrer Wärme und Schönheit .
Aufgrund der Nähe des Geschäftszentrums ist ideal für städtische Kaufleute, die eine Qualitäts-Tagesmenü .
Aber auch am Abend ist ideal für alle Arten von Feiern , von kleinen Empfänge, Geschäftsessen bis hin zu Hochzeiten .
Dvori kralja Tomislava Restaurant können 120 Gäste für eine Hochzeitsfeier und andere Veranstaltungen für bis zu 200 Personen Platz.
</p>

<p>
Dvori kralja Tomislava ist ein Restaurant, das kreative und traditionelle Speisen mit einer warmen Atmosphäre und qualitativ hochwertige Auswahl an Weinen kombiniert .
Die Aromen sind sauber und feine Küche, aus frischen Zutaten , und kocht sehr kreativ und bereit, jeden kulinarischen Wunsch des Gastes zu befriedigen .
Unser erfahrenes Team von Köchen ; Snowy , Peter , Anamaria , Anthony und Mary sein Talent schalten Sie alle Ihre Ideen in köstliche Gerichte auf den Platten ; der inländischen ,
über das Mittelmeer bis hin zur Fusion-Küche .
Sie sind verantwortlich für alles , mit Ausnahme von Übergewicht und die Abhängigkeit von unserer Nahrung.
</p>

<p>
Das Restaurant Dvori kralja Tomislava fühlen Sie sich wie Sie ein Teil der Familie sind . Die Inneneinrichtung ist sehr schön, komplett mit bis Beleuchtung, Skulpturen von Engeln ,
Holzpferd und zuvorkommendes Personal . Das ganze Gebiet bietet ein Gefühl von Wohlbefinden,
sehr sorgfältig mit vielen interessanten Details, die mit viel Liebe in Kraft gesetzt werden eingerichtet. Es Wärme und
lieben unsere Gäste sofort zu erkennen und immer sie erwartungsvoll zurück .
</p>

