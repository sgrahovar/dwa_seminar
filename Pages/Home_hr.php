<h2>O restoranu</h2>

<p>
Restoran Dvori kralja Tomislava uvijek će vas iznova iznenaditi svojom toplinom i ljepotom.
Zbog blizine business centra idealan je za urbane poslovne ljude, koji žele kvalitetnu dnevnu ponudu jela. 
No isto tako, u večernjim satima idealan je za sve oblike proslava, od manjih domjenaka, poslovnih večera do svadbenih svečanosti. 
Restoran Dvori kralja Tomislava može ugostiti 120 uzvanika za svadbeno slavlje , a za druge evente čak i do 200 osoba.
</p>

<p>
Dvori kralja Tomislava je restoran koji ujedinjuje kreativnu i tradicionalnu hranu s toplim ambijentom i kvalitetnim izborom vina. 
Okusi hrane istančano su čisti, spravljeni od svježih namirnica, a kuhari vrlo kreativni i spremni udovoljiti svakoj gastronomskoj želji gosta. 
Naš iskusan tim kuhara; Snješka, Petar, Anamarija, Ante i Meri svojim talentom pretvaraju sve vaše zamisli u ukusna jela na tanjurima ; od domaće, 
preko mediteranske pa sve do fusion kuhinje.
Odgovorni su za sve, osim za višak kilograma i ovisnost o našoj hrani.
</p>

<p>
U restoranu Dvori kralja Tomislava imat ćete osjećaj kao da ste dio obitelji. Interijer je vrlo ugodan, upotpunjen igrom rasvjete, skulpturama anđela, 
drvenih konjića te nadasve ljubaznim osobljem. Cijeli prostor pruža osjećaj ugode i opuštenosti, 
vrlo brižno uređen s mnoštvom zanimljivih detalja koja su donešena s puno ljubavi. Tu toplinu i 
ljubav naši gosti odmah prepoznaju i uvijek joj se s iščekivanjem vraćaju. 
</p>

